public class ode9 {
    public static void main(String[] args) {
        System.out.println("INCERMENT !");
        int a=13;
        int b=++a + ++a;
        System.out.println(b);

        System.out.println("DECREMENT !");
        int c=17;
        int d=--c + --c;
        System.out.println(d);
    }
}
