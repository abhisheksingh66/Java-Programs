
public class code7 {
    public static void main(String[] args) {
        System.out.println("PRE-INCERMENT !");
        int a = 5;
        System.out.println(++a);
        System.out.println(a);

        System.out.println("DECREMENT !");
        int b = 7;
        System.out.println(--b);
        System.out.println(b);
    }
}
